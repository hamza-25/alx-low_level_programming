int add(int a, int b)
{
	int sum;

	sum = a + b;
	return (sum);
}
int sub(int a, int b)
{
        int diff;

        diff = a - b;
        return (diff);
}
int mul(int a, int b)
{
        int mult;

        mult = a * b;
        return (mult);
}
int div(int a, int b)
{
        int rslt;

        rslt = a / b;
        return (rslt);
}
int mod(int a, int b)
{
        int rest;

        rest = a % b;
        return (rest);
}
