#include <stdio.h>
#include "3-calc.h"

/**
 * function - functions that do operation
 * Return: int value 
*/

int op_add(int a, int b)
{
	return (a + b); 
}
int op_sub(int a, int b)
{
	return (a - b);
}
int op_mul(int a, int b)
{
	return (a * b);
}
int op_div(int a, int b)
{
	return (a / b);
}
int op_mod(int a, int b)
{
	return (a % b);
}
