#include "3-calc.h"

/**
 * op_add - functions that do operation
 * @a: first int
 * @b: second in
 * Return: int sum 
*/

int op_add(int a, int b)
{
	return (a + b); 
}

/**
 * op_sub - functions that do operation
 * @a: first int
 * @b: second in
 * Return: int sub
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - functions that do operation
 * @a: first int
 * @b: second in
 * Return: int mult
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - functions that do operation
 * @a: first int
 * @b: second in
 * Return: int div
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - functions that do operation
 * @a: first int
 * @b: second in
 * Return: int mod
*/

int op_mod(int a, int b)
{
	return (a % b);
}
